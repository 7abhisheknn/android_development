package com.example.d0002_view_pager_2;

public class SliderItem {
    private String text;
    private String color;

    SliderItem(String text, String color) {
        this.text = text;
        this.color = color;
    }

    public String getText() {
        return text;
    }
    public String getColor() {
        return color;
    }
}
